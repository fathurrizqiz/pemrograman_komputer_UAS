    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (Txtnama.getText().isEmpty() && txtpassword.getText().isEmpty() && Txtalamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "harap melengkapi data");
        } else {
            try {

                Connection M = Koneksi.koneksi_database();

                String insertQuery = "INSERT INTO db_user (Nama,Password,Gender,Bagian, Alamat,nomor_karyawan) VALUES (?, ?, ?, ?,?,?)";
                PreparedStatement preparedStatement = M.prepareStatement(insertQuery);
                preparedStatement.setString(1, Txtnama.getText());
                preparedStatement.setString(2, txtpassword.getText());
                preparedStatement.setString(3, txtGender.getSelectedItem().toString());
                preparedStatement.setString(4, Txtbagian.getSelectedItem().toString());
                preparedStatement.setString(5, Txtalamat.getText());
                preparedStatement.setString(6, Nomor_karyawan.getText().toString());
                preparedStatement.executeUpdate();

                M.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "harap melengkapi data" + e.getMessage());
            }
            JOptionPane.showMessageDialog(this, "success in saved data");
        }
        dispose();
        new LoginAbsen().setVisible(true);

    }//GEN-LAST:event_jButton1ActionPerformed

