    private void tutupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tutupActionPerformed
        // TODO add your handling code here:
        char echoChar = '*'; // Karakter yang digunakan untuk menggantikan teks

        // Dapatkan teks dari JPasswordField
        char[] password = txtpassword.getPassword();

        // Periksa apakah tombol digunakan untuk menampilkan atau menyembunyikan teks
        if (txtpassword.getEchoChar() == 0) {
            // Jika sebelumnya ditampilkan, sekarang sembunyikan
            txtpassword.setEchoChar(echoChar);
        } else {
            // Jika sebelumnya disembunyikan, sekarang tampilkan
            txtpassword.setEchoChar((char) 0);
        }
    }//GEN-LAST:event_tutupActionPerformed

