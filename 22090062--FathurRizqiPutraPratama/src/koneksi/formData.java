    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        try {
            st = kon.createStatement();

            if (txtNIM.getText().equals("") || txtNama.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "maaf inputan harus di isi");
                return;
            }
            String cekQuery = "SELECT * FROM daftar_mahasiswa WHERE NIM = '" + txtNIM.getText() + "'";
            rs = st.executeQuery(cekQuery);
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "data sudah ada");
            } else {
                String addData = "INSERT INTO daftar_mahasiswa VALUES ('" + txtNIM.getText() + "','" + txtNama.getText()
                        + "','" + CBAngkatan.getSelectedItem() + "')";
                st.executeUpdate(addData);
                clear();
              
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_saveBtnActionPerformed

