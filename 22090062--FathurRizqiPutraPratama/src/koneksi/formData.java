    public void ShowData() {
        try {
            st = kon.createStatement();
            rs = st.executeQuery("SELECT * FROM daftar_mahasiswa");

            DefaultTableModel model = new DefaultTableModel();

            model.addColumn("NIM");
            model.addColumn("Nama");
            model.addColumn("Angkatan");

            model.getDataVector().removeAllElements();
            model.setRowCount(0);
            model.fireTableDataChanged();

            while (rs.next()) {
                Object[] data = {
                    rs.getInt("NIM"),
                    rs.getString("Nama"),
                    rs.getInt("Angkatam")
                };
                model.addRow(data);
                tableMahasiswa.setModel(model);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

